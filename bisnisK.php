        <h1>Bisnis Kontemporer</h1>
        
        <h2>A. Pengertian Bisnis Kontemporer</h2>
        <p>Dalam perspektif Islam, bisnis (tijarah) adalah kegiatan ekonomi yang dilakukan untuk memperoleh keuntungan secara halal dan thayyib, dengan tetap memperhatikan nilai-nilai syariah. Bisnis Kontemporer Islami adalah praktik bisnis modern yang dijalankan dengan tetap mematuhi prinsip-prinsip Islam, baik dalam cara memperoleh, mengelola, maupun mendistribusikan keuntungan.</p>
        <p>Ciri khasnya adalah:</p>
        <ul>
            <li>Bebas dari unsur riba (bunga), gharar (ketidakjelasan), dan maysir (spekulasi atau judi)</li>
            <li>Menjunjung tinggi etika, keadilan, transparansi, dan amanah</li>
            <li>Tidak menjual barang/jasa yang haram (misal: alkohol, pornografi, dsb)</li>
            <li>Bertujuan untuk mencari keberkahan dan ridha Allah SWT, bukan sekadar profit</li>
        </ul>
        
        <h2>B. Dasar-Dasar Regulasi Bisnis</h2>
        <p>Regulasi bisnis dalam Islam didasarkan pada prinsip fiqh muamalah, yakni hukum-hukum Islam yang mengatur hubungan sosial dan transaksi.</p>
        <p>Dasar-dasar fiqih bisnis kontemporer meliputi:</p>
            <p>1. Al-Qur'an Dan Hadis</p>
            <ul><li>Misalnya QS. Al-Baqarah: 275: “Allah menghalalkan jual beli dan mengharamkan riba.”</li></ul>
            <p>2. Ijma' dan Qiyas</p>
            <ul><li>Kesepakatan para ulama dan analogi hukum terhadap kasus-kasus baru.</li></ul>
            <p>3. Kaedah Fiqiyah</p>
            <ul><li>“Al-ashlu fil mu’amalat al-ibahah” (pada dasarnya semua bentuk muamalah diperbolehkan kecuali ada dalil yang melarang).</li>
            <li>“La dharar wa la dhiraar” (tidak boleh membahayakan atau merugikan).</li></ul>
            <p>4. Etika Syariah</p>
            <ul><li>Jujur (shidq), adil (‘adl), tanggung jawab (amanah), dan menjauhi penipuan (gharar).</li></ul>
            <p>5. Fatwa DSN-MUI (Dewan Syariah Nasional - Majelis Ulama Indonesia)</p>
            <ul><li>Menjadi pedoman dalam praktik bisnis modern seperti leasing syariah, e-commerce, fintech syariah, dll.</li></ul>
        
        <h2>C. Tujuan Bisnis</h2>
        <p>Tujuan utama bisnis dalam Islam bukan semata-mata mencari keuntungan duniawi, melainkan untuk meraih ridha Allah SWT, serta berkontribusi terhadap kesejahteraan umat.</p>
        <p>Tujuan-tujuan tersebut meliputi:</p>
        <ul>
            <li>Menegakkan keadilan ekonomi: Distribusi kekayaan yang merata dan adil</li>
            <li>Memenuhi kebutuhan halal masyarakat: Menyediakan produk dan jasa yang halal dan bermanfaat</li>
            <li>Menciptakan lapangan kerja: Mendorong pertumbuhan ekonomi yang berlandaskan keadilan sosial</li>
            <li>Menunaikan kewajiban zakat dan sedekah: Sebagai wujud tanggung jawab sosial</li>
            <li>Mendapatkan keberkahan dan pahala: Menjadikan bisnis sebagai sarana ibadah kepada Allah SWT</li>
        </ul>
        
        <h2>D. Aplikasi E-Commerce dalam Bisnis Kontemporer Islami</h2>
        <p>E-Commerce atau perdagangan elektronik adalah metode jual beli barang atau jasa melalui internet. Dalam Islam, e-commerce dibolehkan selama mematuhi prinsip-prinsip syariah.</p>
        <p>Beberapa syarat dan prinsip e-commerce Islami:</p>
        <ul>
            <li>Produk yang dijual halal dan thayyib</li>
            <li>Transparansi informasi: Tidak ada penipuan dalam deskripsi produk</li>
            <li>Transparansi informasi: Tidak ada penipuan dalam deskripsi produk</li>
            <li>Tidak mengandung unsur riba atau spekulasi berlebihan (maysir)</li>
            <li>Menghindari manipulasi harga atau review palsu</li>
        </ul>
        <p>Contoh aplikasi e-commerce dalam bisnis Islami:</p>
        <ul>
            <li>Marketplace syariah (misalnya: Tokopedia Salam, Hijup, Aqiqah online)</li>
            <li>Penggunaan payment gateway syariah (misalnya: LinkAja Syariah)</li>
            <li>Digitalisasi zakat, infaq, dan sedekah</li>
            <li>Dropshipping atau reseller dengan akad yang sah sesuai syariah</li>
        </ul>
