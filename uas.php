        <h1>Lembaga Keuangan Syariah Perbankan</h1>
        
        <h2>A. Pengertian Lembaga Keuangan Syariah</h2>
        <p>Lembaga keuangan syariah perbankan adalah institusi keuangan yang beroperasi berdasarkan prinsip-prinsip syariah Islam. Prinsip ini melarang praktik riba (bunga), gharar (ketidakjelasan), dan maysir (spekulasi). Sebagai gantinya, lembaga ini menerapkan sistem bagi hasil, jual beli, dan sewa dalam akad-akadnya.</p>
        <p>Contoh akad yang digunakan:</p>
        <ul>
            <li>Mudharabah (kerja sama antara pemilik modal dan pengelola)</li>
            <li>Musyarakah (kemitraan usaha)</li>
            <li>Murabahah (jual beli dengan margin keuntungan)</li>
            <li>Ijarah (sewa menyewa)</li>
        </ul>
        
        <h2>Karakteristik Lembaga Keuangan Syariah</h2>
        <ul>
            <li>Bebas Riba: Semua bentuk bunga dilarang, karena dianggap menindas dan tidak adil.</li>
            <li>Bagi Hasil: Keuntungan dan kerugian dibagi sesuai dengan kesepakatan dalam kontrak.</li>
            <li>Akad Islami: Setiap transaksi didasarkan pada perjanjian yang sesuai syariah.</li>
            <li>Pengawasan Syariah: Diawasi oleh Dewan Pengawas Syariah (DPS) untuk menjamin kesesuaian dengan prinsip Islam</li>
        </ul>
        
        <h2>B. Pengertian Lembaga Keuangan Non-Syariah</h2>
        <p>Lembaga keuangan non-syariah atau konvensional adalah institusi yang beroperasi dengan sistem bunga dalam transaksi keuangan. Bank konvensional mendapatkan keuntungan dari selisih antara bunga yang dibayarkan kepada penabung dan bunga yang dikenakan kepada peminjam.</p>
        
