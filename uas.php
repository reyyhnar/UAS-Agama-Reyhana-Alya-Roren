        <h2>Bagaimana Semestinya Penanganan dan Pencegahan Kasus KSP Indosurya</h2>
            <p>1. Pengawasan Preventif Harus Lebih Proaktif</p>
            <p>KSP Indosurya berhasil menghimpun dana dari masyarakat dalam jumlah sangat besar (hingga Rp 106 triliun), padahal tidak memiliki izin sebagai lembaga penghimpun dana masyarakat seperti bank.</p>
            <p>Semestinya:</p>
            <ul>
                <li>OJK, Kementerian Koperasi, dan Satgas Waspada Investasi perlu membentuk sistem deteksi dini berbasis laporan keuangan, promosi publik yang mencurigakan (misalnya bunga tinggi tak masuk akal), dan aduan masyarakat.</li>
                <li>Koperasi yang menjanjikan keuntungan di atas pasar seharusnya langsung diperiksa dan diberi peringatan dini, bukan hanya setelah masalah membesar.</li>
             </ul>
            
            <p>2. Pemisahan Fungsi Koperasi Simpan Pinjam dan Investasi</p>
            <p>Salah satu akar masalah adalah kaburnya batas antara kegiatan koperasi simpan pinjam dengan skema investasi.</p>
            <p>Semestinya:</p>
            <ul>
                <li>Regulasi koperasi diperkuat untuk melarang praktik investasi dana masyarakat dengan imbal hasil tetap tinggi, kecuali melalui izin dan pengawasan ketat.</li>
                <li>Koperasi tidak boleh melakukan pengumpulan dana yang menyerupai deposito jika tidak diawasi oleh OJK atau Bank Indonesia.</li>
             </ul>

            <p>3. Koordinasi Lintas Lembaga Diperkuat</p>
            <p>Kasus Indosurya menunjukkan lemahnya koordinasi antara OJK, Kementerian Koperasi, dan aparat penegak hukum.</p>
            <p>Semestinya:</p>
            <ul>
                <li>Perlu dibentuk satuan tugas nasional khusus menangani keuangan non-bank ilegal, dengan wewenang lintas lembaga dan sistem pelaporan terpadu.</li>
             </ul>

